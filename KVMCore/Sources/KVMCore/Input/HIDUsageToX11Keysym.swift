public enum HIDUsageToX11Keysym {
    public struct KeyTransition: Equatable, Sendable {
        public let keysym: UInt32
        public let isDown: Bool

        public init(keysym: UInt32, isDown: Bool) {
            self.keysym = keysym
            self.isDown = isDown
        }
    }

    private static let table: [UInt8: UInt32] = [
