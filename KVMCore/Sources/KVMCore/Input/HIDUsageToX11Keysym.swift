    private static let modifierTable: [HIDModifierBit: UInt32] = [
        .leftControl: 0xffe3,
        .leftShift: 0xffe1,
        .leftAlt: 0xffe9,
        .leftGUI: 0xffeb,
        .rightControl: 0xffe4,
        .rightShift: 0xffe2,
        .rightAlt: 0xffea,
        .rightGUI: 0xffec,
    ]

    private static let shiftedTable: [UInt8: UInt32] = [
        0x04: 0x0041, 0x05: 0x0042, 0x06: 0x0043, 0x07: 0x0044, 0x08: 0x0045,
