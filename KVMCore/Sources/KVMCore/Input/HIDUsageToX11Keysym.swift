        0x2D: 0x005F, 0x2E: 0x002B, 0x2F: 0x007B, 0x30: 0x007D, 0x31: 0x007C,
        0x32: 0x007C, 0x33: 0x003A, 0x34: 0x0022, 0x35: 0x007E, 0x36: 0x003C,
        0x37: 0x003E, 0x38: 0x003F,
    ]

    public static func lookup(usage: UInt8) -> UInt32? {
        table[usage]
    }

    public static func lookup(usage: UInt8, modifier: UInt8) -> UInt32? {
        if modifier.hasShift, let shifted = shiftedTable[usage] {
            return shifted
        }
        return lookup(usage: usage)
    }

    public static func modifierKeysym(for bit: HIDModifierBit) -> UInt32 {
        modifierTable[bit]!
    }

    public static func transitions(from previous: HIDKeyboardReport, to current: HIDKeyboardReport) -> [KeyTransition] {
        var transitions: [KeyTransition] = []
        let previousKeys = Set(previous.keycodes.filter { $0 != 0 })
        let currentKeys = Set(current.keycodes.filter { $0 != 0 })

        for usage in previous.keycodes where usage != 0 && !currentKeys.contains(usage) {
            if let keysym = lookup(usage: usage, modifier: previous.modifier) {
                transitions.append(KeyTransition(keysym: keysym, isDown: false))
            }
        }

        appendModifierTransitions(to: &transitions, from: previous, to: current, isDown: false)
        appendModifierTransitions(to: &transitions, from: previous, to: current, isDown: true)

        for usage in current.keycodes where usage != 0 && !previousKeys.contains(usage) {
            if let keysym = lookup(usage: usage, modifier: current.modifier) {
                transitions.append(KeyTransition(keysym: keysym, isDown: true))
            }
        }

        return transitions
    }

    public static func repeatTransitions(for report: HIDKeyboardReport) -> [KeyTransition] {
        guard
            let usage = report.keycodes.last(where: { $0 != 0 }),
            let keysym = lookup(usage: usage, modifier: report.modifier)
        else { return [] }

        return [
            KeyTransition(keysym: keysym, isDown: false),
            KeyTransition(keysym: keysym, isDown: true),
        ]
    }

    private static func appendModifierTransitions(
        to transitions: inout [KeyTransition],
        from previous: HIDKeyboardReport,
        to current: HIDKeyboardReport,
        isDown: Bool
    ) {
        for bit in HIDModifierBit.allCases {
            let mask = bit.rawValue
            let wasDown = (previous.modifier & mask) != 0
            let nowDown = (current.modifier & mask) != 0
            guard wasDown != nowDown, nowDown == isDown else { continue }
            transitions.append(KeyTransition(keysym: modifierKeysym(for: bit), isDown: isDown))
        }
    }
}

private extension UInt8 {
    var hasShift: Bool {
        self & (HIDModifierBit.leftShift.rawValue | HIDModifierBit.rightShift.rawValue) != 0
    }
}
